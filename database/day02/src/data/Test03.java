package data;

public class Test03 {
	public static void main(String[] args) {
		//변수(Variable) = 데이터 기억 장치
		//변수를 이용하여 유지보수가 쉬움
		
		int a =  7000*2;//7000*2를 계산해 a라는 이름으로 저장(대입)
		int b = 8000*3;//8000*2를 계산해 b라는 이름으로 저장(대입)
		
		System.out.println(a);//a에 저장된 데이터를 출력
		System.out.println(b);//b에 저장된 데이터를 출력
		System.out.println(a + b);
	}
}
